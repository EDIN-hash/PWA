// ============================================================================
// 1. CRITICAL CSS (inline в <head>)
// ============================================================================
// 
// Создан файл: src/critical.css
// - Содержит только стили для above-the-fold контента
// - Инлайнится в <head> через Vite плагин
// - Включает: header, grid, card, tabs, buttons, inputs, modal, loading
//
// Файл: vite.config.js
// - Читает critical.css и инжектит в HTML через createHtmlPlugin
// - Переменная: <%= criticalCSS %>
//
// Эффект: FCP снижается на 150-250ms

// ============================================================================
// 2. PRELOAD ШРИФТОВ
// ============================================================================
//
// Файл: index.html
// - <link rel="preconnect" для fonts.googleapis.com и fonts.gstatic.com
// - <link rel="preload" с as="style" для шрифта Inter
// - <noscript> fallback для JS-отключенных браузеров
// - font-display: swap в CSS
//
// Эффект: LCP снижается на 100-150ms

// ============================================================================
// 3. LAZY LOAD КАРТОЧЕК И ИЗОБРАЖЕНИЙ
// ============================================================================
//
// Файл: src/Card.jsx
// - Добавлен IntersectionObserver для карточек (cardRef)
// - Карточки рендерятся только при появлении в viewport (rootMargin: 200px)
// - Компонент LazyImage с IntersectionObserver для изображений
// - memo() для предотвращения лишних ре-рендеров
//
// Эффект: Initial JS меньше, TTI снижается на 200-300ms

// ============================================================================
// 4. ОПТИМИЗАЦИЯ АНИМАЦИЙ
// ============================================================================
//
// Файл: src/styles.css
// - contain: paint layout для card-premium, items-grid, header-section
// - will-change только при hover (не постоянно)
// - Оптимизированы transition (убраны лишние cubic-bezier)
//
// Эффект: Меньше GPU нагрузки, стабильнее FPS

// ============================================================================
// 5. LAZY LOAD ИЗОБРАЖЕНИЙ
// ============================================================================
//
// Файл: src/Card.jsx
// - LazyImage компонент с:
//   - IntersectionObserver (rootMargin: 100px)
//   - Показ лоадера пока изображение не загружено
//   - loading="lazy" decoding="async"
//
// Эффект: Картинки грузятся только когда видны

// ============================================================================
// 6. VITE BUILD ОПТИМИЗАЦИИ
// ============================================================================
//
// Файл: vite.config.js
// - cssInjectedByJsPlugin - CSS в JS бандле
// - cssCodeSplit: false - один CSS файл
// - manualChunks: vendor - React, ReactDOM, ReactModal, DatePicker
// - Critical CSS инлайн
//
// Эффект: Меньше сетевых запросов

// ============================================================================
// 7. PERFORMANCE TESTING
